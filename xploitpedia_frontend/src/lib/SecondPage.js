export function whichCategory (category) {

    if ((category !== 'Indicators Of Compromise') && (category !== 'Intrusion Tools')) {
        return ((category === 'Threat Groups' ?  'threat_groups': category.toLowerCase()))
    }
    else {
        return((category === 'Intrusion Tools'? 'tools' : 'iocs'))

    }
}

